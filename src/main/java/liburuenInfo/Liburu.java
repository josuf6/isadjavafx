package liburuenInfo;

public class Liburu {
    String title;
    String subtitle;
    String[] publishers;
    int number_of_pages;

    public Liburu() {
    }

    public String getTitle() {
        return title;
    }

    public String getSubtitle() {
        return subtitle;
    }

    public String[] getPublishers() {
        return publishers;
    }

    public int getNumber_of_pages() {
        return number_of_pages;
    }
}
